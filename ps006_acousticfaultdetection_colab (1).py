# Cell 5 — Feature functions

def time_domain_features(frame):
    """
    7 features. All physically interpretable.
    Kurtosis is the single most important — spikes during bearing faults.
    """
    rms      = np.sqrt(np.mean(frame ** 2)) + 1e-8
    peak     = np.max(np.abs(frame))
    mean_abs = np.mean(np.abs(frame)) + 1e-8
    mu       = np.mean(frame)
    sd       = np.std(frame) + 1e-8

    zcr      = np.sum(np.diff(np.sign(frame)) != 0) / len(frame)
    crest    = peak / rms
    impulse  = peak / mean_abs
    kurtosis = np.mean(((frame - mu) / sd) ** 4)   # KEY FEATURE
    skewness = np.mean(((frame - mu) / sd) ** 3)
    shape    = rms / mean_abs

    return np.array([rms, zcr, crest, impulse, kurtosis, skewness, shape],
                    dtype=np.float32)


def frequency_domain_features(frame, sr=SR):
    """
    9 features. Captures spectral shape, band energies, noise structure.
    """
    N       = len(frame)
    window  = np.hanning(N)
    fft_mag = np.abs(np.fft.rfft(frame * window))
    freqs   = np.fft.rfftfreq(N, 1.0 / sr)
    fft_nrm = fft_mag / (np.sum(fft_mag) + 1e-8)

    # Spectral shape
    centroid = np.sum(freqs * fft_nrm)
    spread   = np.sqrt(np.sum(((freqs - centroid) ** 2) * fft_nrm) + 1e-8)
    cumsum   = np.cumsum(fft_nrm)
    rolloff  = freqs[min(np.searchsorted(cumsum, 0.85), len(freqs) - 1)]

    # Band energy ratios (fault signatures live here)
    bands    = [(0, 500), (500, 2000), (2000, 8000), (8000, 11025)]
    energies = []
    for lo, hi in bands:
        mask = (freqs >= lo) & (freqs < hi)
        energies.append(np.sum(fft_mag[mask] ** 2))

    # Harmonic-to-noise ratio + spectral entropy
    total_e = np.sum(fft_mag ** 2) + 1e-8
    peak_e  = np.max(fft_mag) ** 2
    hnr     = peak_e / (total_e - peak_e + 1e-8)
    entropy = -np.sum(fft_nrm * np.log(fft_nrm + 1e-8))

    return np.array([centroid, spread, rolloff] + energies + [hnr, entropy],
                    dtype=np.float32)


def mfcc_features(frame, sr=SR):
    """
    52 features.
    MFCC mean + std captures spectral shape.
    Delta captures rate of change (fault onset).
    Delta-delta captures acceleration (fault progression).
    """
    mfcc    = librosa.feature.mfcc(y=frame, sr=sr, n_mfcc=N_MFCC,
                                    n_fft=N_FFT, hop_length=HOP_FFT,
                                    n_mels=N_MELS)
    delta1  = librosa.feature.delta(mfcc)
    delta2  = librosa.feature.delta(mfcc, order=2)

    return np.concatenate([
        np.mean(mfcc,   axis=1),   # 13 — average spectral shape
        np.std(mfcc,    axis=1),   # 13 — spectral variation (rises with fault)
        np.mean(delta1, axis=1),   # 13 — spectral velocity
        np.mean(delta2, axis=1),   # 13 — spectral acceleration
    ]).astype(np.float32)


def extract_features(frame, sr=SR):
    """
    Full 68-feature vector from one audio frame.
    7 (time) + 9 (frequency) + 52 (MFCC) = 68
    """
    td = time_domain_features(frame)
    fd = frequency_domain_features(frame, sr)
    mf = mfcc_features(frame, sr)
    return np.concatenate([td, fd, mf])


