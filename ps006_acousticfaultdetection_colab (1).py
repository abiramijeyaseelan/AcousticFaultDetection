# Cell 13 — Convert both models to INT8 TFLite

def convert_to_tflite_int8(model, rep_data, output_path, name):
    """
    INT8 quantization with float32 I/O.
    IMPORTANT: float32 I/O prevents zero-output bug on ESP32-S3.
    This is a known TFLite 2.19 issue — do NOT change I/O types.
    """
    converter = tf.lite.TFLiteConverter.from_keras_model(model)
    converter.optimizations = [tf.lite.Optimize.DEFAULT]

    sample_indices = np.random.choice(
        len(rep_data), min(300, len(rep_data)), replace=False)

    def representative_dataset():
        for i in sample_indices:
            yield [rep_data[i:i+1].astype(np.float32)]

    converter.representative_dataset       = representative_dataset
    converter.target_spec.supported_ops    = [tf.lite.OpsSet.TFLITE_BUILTINS_INT8]
    converter.inference_input_type         = tf.float32   # ← KEEP float32
    converter.inference_output_type        = tf.float32   # ← KEEP float32
    converter.experimental_new_quantizer   = True

    tflite_bytes = converter.convert()

    with open(output_path, 'wb') as f:
        f.write(tflite_bytes)

    kb = len(tflite_bytes) / 1024
    print(f"  {name}: {kb:.1f} KB")
    return tflite_bytes


