import math

def simulate_esp32_pipeline(raw_features, label_name):
    """
    Replicates the EXACT inference sequence that runs on ESP32-S3.
    Step 1: Normalize with SCALER_MEAN / SCALER_STD
    Step 2: Clip to [-6, 6]
    Step 3: Autoencoder → reconstruction error
    Step 4: Compare to THRESHOLD
    Step 5: If anomaly → run classifier
    Step 6: Compute health score (sigmoid)
    Step 7: Recommend action
    """
    # Step 1-2: Normalize and clip (matches ESP32 firmware exactly)
    scaled = (raw_features - scaler.mean_) / scaler.scale_
    scaled = np.clip(scaled, -6.0, 6.0).astype(np.float32)

    # Step 3: Autoencoder reconstruction
    recon    = run_tflite_inference(ae_tflite_bytes, scaled)
    ae_error = float(np.mean((scaled - recon[0]) ** 2))

    # Step 4: Threshold comparison
    is_anomaly = ae_error > THRESHOLD

    # Step 5: Classifier (only when anomaly detected — saves compute)
    if is_anomaly:
        probs      = run_tflite_inference(clf_tflite_bytes, scaled)[0]
        fault_idx  = int(probs.argmax())
        fault_name = FAULT_NAMES_PY[fault_idx]
        confidence = float(probs.max()) * 100
    else:
        fault_name = 'healthy'
        confidence = 100.0

    # Step 6: Health score — sigmoid mapping
    normalized_err = ae_error / (THRESHOLD + 1e-8)
    health_score   = 100.0 / (1.0 + math.exp(normalized_err * 3.0 - 3.0))
    health_score   = max(0.0, min(100.0, health_score))

    # Step 7: Action recommendation
    if health_score > 80:
        action = "✅ No action needed"
        led    = "GREEN"
    elif health_score > 60:
        action = "⚠️  Schedule inspection this week"
        led    = "YELLOW"
    elif health_score > 40:
        action = "🔶 Reduce load — inspect within 24h"
        led    = "ORANGE"
    else:
        action = "🚨 STOP MACHINE — Immediate inspection required"
        led    = "RED"

    print(f"\n  ┌─ Input: {label_name}")
    print(f"  │  AE Error:     {ae_error:.4f}  (threshold: {THRESHOLD:.4f})")
    print(f"  │  Anomaly:      {is_anomaly}")
    print(f"  │  Fault type:   {fault_name} ({confidence:.1f}% confidence)")
    print(f"  │  Health score: {health_score:.1f} / 100")
    print(f"  │  LED:          {led}")
    print(f"  └─ Action:       {action}")
    return health_score, is_anomaly


