def build_autoencoder(input_dim=INPUT_DIM):
    inputs  = keras.Input(shape=(input_dim,))

    # Encoder
    x = keras.layers.Dense(48, activation='relu')(inputs)
    x = keras.layers.BatchNormalization()(x)
    x = keras.layers.Dense(24, activation='relu')(x)
    x = keras.layers.BatchNormalization()(x)
    encoded = keras.layers.Dense(8, activation='relu', name='bottleneck')(x)

    # Decoder
    x = keras.layers.Dense(24, activation='relu')(encoded)
    x = keras.layers.BatchNormalization()(x)
    x = keras.layers.Dense(48, activation='relu')(x)
    outputs = keras.layers.Dense(input_dim, activation='linear')(x)

    ae = keras.Model(inputs, outputs, name='autoencoder')
    ae.compile(optimizer=keras.optimizers.Adam(0.001), loss='mse')

    return ae

