def build_autoencoder(input_dim):
    """
    Encoder: 68 → 48 → 24 → 8  (compression)
    Decoder: 8  → 24 → 48 → 68 (reconstruction)

    Trained on HEALTHY data only.
    Healthy sound → low reconstruction error → high health score
    Fault sound   → high reconstruction error → low health score
    """
    inputs  = tf.keras.Input(shape=(input_dim,), name='input')

    # Encoder
    x = tf.keras.layers.Dense(48, activation='relu', name='enc1')(inputs)
    x = tf.keras.layers.BatchNormalization(name='bn1')(x)
    x = tf.keras.layers.Dense(24, activation='relu', name='enc2')(x)
    x = tf.keras.layers.BatchNormalization(name='bn2')(x)
    encoded = tf.keras.layers.Dense(8, activation='relu', name='bottleneck')(x)

    # Decoder
    x = tf.keras.layers.Dense(24, activation='relu', name='dec1')(encoded)
    x = tf.keras.layers.BatchNormalization(name='bn3')(x)
    x = tf.keras.layers.Dense(48, activation='relu', name='dec2')(x)
    x = tf.keras.layers.BatchNormalization(name='bn4')(x)
    outputs = tf.keras.layers.Dense(input_dim, activation='linear',
                                     name='reconstruction')(x)

    model = tf.keras.Model(inputs, outputs, name='autoencoder')
    model.compile(
        optimizer=tf.keras.optimizers.Adam(learning_rate=0.001),
        loss='mse',
        metrics=['mae']
    )
    return model


