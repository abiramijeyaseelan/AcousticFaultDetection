    header = f'// Auto-generated — do not edit manually\n'
    header += f'// Model size: {len(data_bytes)/1024:.1f} KB\n\n'
    header += f'#ifndef {var_name.upper()}_H\n'
    header += f'#define {var_name.upper()}_H\n\n'
    header += f'alignas(8) const unsigned char {var_name}[] = {{\n  {formatted}\n}};\n'
    header += f'const unsigned int {var_name}_len = {len(data_bytes)};\n\n'
    header += f'#endif // {var_name.upper()}_H\n'
    with open(output_path, 'w') as f:
        f.write(header)
    print(f'Written: {output_path} ({len(data_bytes)/1024:.1f} KB)')

