# Cell 11 — Build and train binary classifier

def build_classifier(input_dim, num_classes):
    """
    Dense neural network for fault classification.
    Runs ONLY when autoencoder flags an anomaly.
    Binary output: healthy (0) vs anomaly (1)

    Architecture:
      Dense(64) → BN → Dropout(0.3)
      Dense(32) → BN → Dropout(0.2)
      Dense(16) → BN
      Dense(num_classes, softmax)
    """
    model = tf.keras.Sequential([
        tf.keras.layers.Input(shape=(input_dim,)),

        tf.keras.layers.Dense(64, activation='relu'),
        tf.keras.layers.BatchNormalization(),
        tf.keras.layers.Dropout(0.3),

        tf.keras.layers.Dense(32, activation='relu'),
        tf.keras.layers.BatchNormalization(),
        tf.keras.layers.Dropout(0.2),

        tf.keras.layers.Dense(16, activation='relu'),
        tf.keras.layers.BatchNormalization(),

        tf.keras.layers.Dense(num_classes, activation='softmax')
    ], name='classifier')

    model.compile(
        optimizer=tf.keras.optimizers.Adam(learning_rate=0.001),
        loss='sparse_categorical_crossentropy',
        metrics=['accuracy']
    )
    return model


