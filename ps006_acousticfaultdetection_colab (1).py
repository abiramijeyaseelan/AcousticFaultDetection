def extract_from_wav_list(wav_files, label, max_files=500, desc=''):
    """
    Extracts feature frames from a list of wav files.
    Uses sliding window with 75% overlap for maximum data.
    """
    X_list, y_list = [], []
    errors = 0

    files_to_use = wav_files[:max_files]
    print(f"Processing {desc}: {len(files_to_use)} files...")

    for i, wav_path in enumerate(files_to_use):
        try:
            audio, _ = librosa.load(str(wav_path), sr=SR, mono=True)
            for start in range(0, len(audio) - FRAME_LEN, HOP_LEN):
                frame = audio[start:start + FRAME_LEN]
                if len(frame) == FRAME_LEN:
                    feat = extract_features(frame, SR)
                    # Strict quality gate — no NaN or Inf allowed
                    if not np.any(np.isnan(feat)) and not np.any(np.isinf(feat)):
                        X_list.append(feat)
                        y_list.append(label)
        except Exception as e:
            errors += 1

        if (i + 1) % 100 == 0:
            print(f"  {i+1}/{len(files_to_use)} files → {len(X_list)} frames")

    print(f"  Done: {len(X_list)} frames | Errors: {errors}")
    return X_list, y_list


