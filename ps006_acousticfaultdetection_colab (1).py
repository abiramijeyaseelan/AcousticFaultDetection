def bytes_to_c_header(data_bytes, var_name, output_path, description=''):
    # Convert to proper hex values
    hex_values = [f'0x{b:02x}' for b in data_bytes]

    # Group safely (12 values per line)
    lines = []
    for i in range(0, len(hex_values), 12):
        lines.append('  ' + ', '.join(hex_values[i:i+12]))

    formatted = ',\n'.join(lines)
    guard = var_name.upper() + '_H'

    header  = f'#ifndef {guard}\n#define {guard}\n\n'
    header += '#include <stdint.h>\n\n'
    header += f'const uint8_t {var_name}[] = {{\n{formatted}\n}};\n'
    header += f'const unsigned int {var_name}_len = {len(data_bytes)};\n\n'
    header += f'#endif\n'

    with open(output_path, 'w') as f:
        f.write(header)

    print(f"✅ Written: {output_path}")


def floats_to_c_array(arr, name):
    # Group safely (4 floats per line)
    lines = []
    for i in range(0, len(arr), 4):
        chunk = ', '.join(f'{v:.8f}f' for v in arr[i:i+4])
        lines.append('  ' + chunk)

    return f'const float {name}[{len(arr)}] = {{\n' + ',\n'.join(lines) + '\n};\n'

import re
import os

def fix_hex_header(input_path, output_path):
    with open(input_path, 'r') as f:
        content = f.read()

    # Find the array content between { and }
    match = re.search(r'\{(.*?)\}', content, re.DOTALL)
    if not match:
        print(f"Could not parse {input_path}")
        return

    # Extract all hex values
    hex_values = re.findall(r'0x[0-9a-fA-F]{2}', match.group(1))

    # Rebuild with 12 per line — never splits mid-value
    lines = []
    for i in range(0, len(hex_values), 12):
        lines.append('  ' + ', '.join(hex_values[i:i+12]))
    new_array = ',\n'.join(lines)

    # Replace the array body
    new_content = content[:match.start(1)] + '\n' + new_array + '\n' + content[match.end(1):]

    with open(output_path, 'w') as f:
        f.write(new_content)
    print(f"Fixed: {output_path}")

def fix_scaler_header(input_path, output_path):
    with open(input_path, 'r') as f:
        content = f.read()

    # Find all float arrays and rebuild them
    def rebuild_float_array(match):
        name_part = match.group(1)
        body = match.group(2)
        # Extract all float values
        floats = re.findall(r'-?[\d]+\.[\d]+f', body)
        # 4 per line
        lines = []
        for i in range(0, len(floats), 4):
            lines.append('  ' + ', '.join(floats[i:i+4]))
        return name_part + '\n' + ',\n'.join(lines) + '\n};'

    new_content = re.sub(
        r'(const float \w+\[\d+\] = \{)(.*?)(\};)',
        lambda m: rebuild_float_array(m),
        content,
        flags=re.DOTALL
    )

    with open(output_path, 'w') as f:
        f.write(new_content)
    print(f"Fixed: {output_path}")

