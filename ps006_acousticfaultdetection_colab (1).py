def convert_to_tflite(model, representative_data, output_path, model_name):
    """INT8 quantization with float32 I/O — avoids zero-output bug."""
    converter = tf.lite.TFLiteConverter.from_keras_model(model)
    converter.optimizations = [tf.lite.Optimize.DEFAULT]

    def rep_dataset():
        idx = np.random.choice(len(representative_data), min(300, len(representative_data)), replace=False)
        for i in idx:
            yield [representative_data[i:i+1].astype(np.float32)]

    converter.representative_dataset = rep_dataset
    # KEEP I/O as float32 — INT8 I/O causes zero-output on ESP32
    converter.target_spec.supported_ops = [tf.lite.OpsSet.TFLITE_BUILTINS_INT8]
    converter.inference_input_type  = tf.float32
    converter.inference_output_type = tf.float32

    tflite_bytes = converter.convert()
    with open(output_path, 'wb') as f:
        f.write(tflite_bytes)

    kb = len(tflite_bytes) / 1024
    print(f'{model_name}: {kb:.1f} KB')
    return tflite_bytes

