def bytes_to_c_header(data_bytes, var_name, output_path, description=''):
    """
    Convert binary model to C header array.
    Pure Python — no xxd needed, works everywhere.
    alignas(8) ensures correct memory alignment for TFLite.
    """
    hex_values = ', '.join(f'0x{b:02x}' for b in data_bytes)
    chunks     = [hex_values[i:i+72] for i in range(0, len(hex_values), 72)]
    formatted  = ',\n  '.join(chunks)
    guard      = var_name.upper() + '_H'

    header  = f'// Auto-generated by PS-006 pipeline — do not edit manually\n'
    header += f'// {description}\n'
    header += f'// Model size: {len(data_bytes)/1024:.1f} KB\n\n'
    header += f'#ifndef {guard}\n#define {guard}\n\n'
    header += f'#include <stdint.h>\n\n'
    header += f'alignas(8) const uint8_t {var_name}[] = {{\n  {formatted}\n}};\n'
    header += f'const unsigned int {var_name}_len = {len(data_bytes)};\n\n'
    header += f'#endif // {guard}\n'

    with open(output_path, 'w') as f:
        f.write(header)
    print(f"  Written: {os.path.basename(output_path)} ({len(data_bytes)/1024:.1f} KB)")


